<h2>LAT Episode 1 - The Beginning</h2>
<p>
In this new series, known as LAT (Life at TSBVI), we will be taking some adventures through the school, and reviewing a couple of activities there.<br>
TSBVI is Texas School for the Blind and Visually Impaired, and its an interesting life to be there.<br>
Anyway, in this first episode, we will be doing a couple of things.<br>
We start off with an introduction and walking toward the Recreation Center.<br>
